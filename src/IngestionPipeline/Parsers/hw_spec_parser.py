def parse(
    path: str,
    *,
    use_llm: bool = True,
    model: str = _DEFAULT_MODEL,
    max_workers: int = 3,
    api_key: Optional[str] = None,
) -> Dict[str, Any]:
    """
    Extract hardware specifications from a Markdown file.

    The input should be a Markdown file produced by ``pdf_parser.parse()``
    from an Infineon hardware-manual PDF (e.g. the
    ``*_FBcontent_INTERNAL_*.pdf`` family under
    ``Alpha2_VP-EIR2_iLLD/Baselined_Versions/RC1_IP_<MODULE>/``).

    Args:
        path: Path to the ``.md`` file.
        use_llm: When True (default), registers and fields are extracted via
            GPT-5.2 on GPT4IFX.  When the LLM client is unavailable or
            produces no registers, the function automatically falls back to
            the regex backend.  Set to False to force regex-only.
        model: LLM model identifier (default: ``gpt-5.2``).
        max_workers: Parallel LLM workers (default: 3).
        api_key: Optional bearer token override; when None the project's
            ``token_manager.ensure_valid_token`` is used.

    Returns:
        A dict with keys ``metadata`` (incl. ``backend`` flag), ``registers``,
        ``fields``, ``interrupts``, ``errors``, ``formulas``,
        ``hw_constraints``, ``programming_sequences``, ``submodules``, and
        ``relationships``.

    Raises:
        FileNotFoundError: If *path* does not exist.
    """
    p = Path(path)
    if not p.exists():
        raise FileNotFoundError(f"File not found: {path}")

    content = p.read_text(encoding="utf-8")
    logger.info(
        "Parsing hardware spec from %s (%d lines, backend=%s)",
        p.name, content.count("\n") + 1, "llm" if use_llm else "regex",
    )

    return _HWSpecExtractor(
        content,
        p.name,
        use_llm=use_llm,
        model=model,
        max_workers=max_workers,
        api_key=api_key,
    ).extract_all()
