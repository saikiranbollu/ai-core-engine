"""
Hardware Specification Parser (Markdown → Structured JSON)
==========================================================

Extracts hardware register specifications from a Markdown file that was
previously generated by ``pdf_parser.py`` from a hardware-manual PDF
(targeted at the RC1 iLLD ``*_FBcontent_INTERNAL_*.pdf`` family found under
``Alpha2_VP-EIR2_iLLD/Baselined_Versions/RC1_IP_<MODULE>/``).

**Two extraction backends:**

* **LLM backend (default)** — registers + fields are extracted via an LLM
  (GPT-5.2 on GPT4IFX, identical client/prompt to ``hw_um_llm_parser.py``).
  Robust against the table-heavy / heading-poor PDF→MD output produced by
  PyMuPDF for RC1 FB-content manuals.
* **Regex fallback** — used automatically when the LLM client cannot be
  initialised (offline, missing token, missing openai) or when the LLM run
  yields zero registers.  Pattern set matches the legacy iLLD
  ``NAME — Description`` + ``Offset address:`` markdown style.

The v3.0 extractors below are **always regex-based** (they mine free prose
and do not need clean register sections):

* Interrupts (also field-derived when LLM backend is used)
* Errors (also field-derived when LLM backend is used)
* Formulas — LaTeX ``$$...$$`` blocks and plain-text equations
* ``HwConstraint`` — timing / wait / ordering / access-width / frequency /
  side-effect
* ``ProgrammingSequence`` + ``SequenceStep`` — Init/Tx/Rx/Shutdown blocks
  with numbered steps and per-step register/field resolution
* Hardware sub-modules — ``CXPI0``/``CXPI1``/``SPI3`` style instances

**6 relationship types built:**

1. ``register_has_field`` — register → field
2. ``error_sets_interrupt`` — error field → interrupt flag
3. ``field_used_in_formula`` — field ↔ formula
4. ``field_enables_feature`` — field → HW feature
5. ``interrupt_masked_by_field`` — (stub, future)
6. ``operation_triggers_interrupt`` — operation → interrupt

Usage::

    from IngestionPipeline.parsers import hw_spec_parser

    # LLM extraction (default) — uses GPT4IFX token_manager:
    result = hw_spec_parser.parse("CXPI_FBcontent_INTERNAL_2025-11-05.md")

    # Force regex-only (no network calls):
    result = hw_spec_parser.parse("cxpi_hw_manual.md", use_llm=False)
"""

