# LLM determinism — fixed seed + on-disk response cache
# ---------------------------------------------------------------------------
# GPT-5.2 is a reasoning model: ``temperature=0`` alone does not guarantee
# bit-identical replies between runs (constraints/errors typically fluctuate
# ±50% in count). We mitigate this with two layers:
#
#   1. Pass a fixed ``seed`` on every chat-completions request. When the
#      backend honours it AND ``system_fingerprint`` is stable, replies are
#      deterministic.
#   2. Cache every successful reply on disk, keyed by a SHA-256 of
#      ``(model, prompt_version, system_prompt, user_content, seed)``. On a
#      re-run a cache hit returns the exact JSON object that was parsed
#      previously — zero network calls, no 429s, second-of-a-millisecond.
#
# Cache layout::
#
#     <cache_root>/<model>/<sha256>.json
#
# Cache root resolves to (in order of precedence):
#
#   * ``$AICE_HW_SPEC_CACHE_DIR`` if set,
#   * else ``~/.cache/aice_hw_spec``.
#
# Set ``$AICE_HW_SPEC_CACHE_DISABLE=1`` to bypass both reads and writes (useful
# when you want to force a fresh LLM run, e.g. to validate prompt changes).

_LLM_SEED = 42
_LLM_PROMPT_VERSION = "v3.0-2026-05"   # bump when extraction/sequence prompt is edited


def _llm_cache_root() -> Path:
    override = os.environ.get("AICE_HW_SPEC_CACHE_DIR")
    if override:
        return Path(override)
    return Path.home() / ".cache" / "aice_hw_spec"


def _llm_cache_disabled() -> bool:
    return os.environ.get("AICE_HW_SPEC_CACHE_DISABLE", "").strip() in {"1", "true", "yes"}


def _llm_cache_key(
    *,
    model: str,
    system_prompt: str,
    user_content: str,
    seed: int,
    extra: str = "",
) -> str:
    """SHA-256 hex digest covering every input that influences the reply."""
    h = hashlib.sha256()
    h.update(_LLM_PROMPT_VERSION.encode("utf-8"))
    h.update(b"\x00")
    h.update(model.encode("utf-8"))
    h.update(b"\x00")
    h.update(str(seed).encode("utf-8"))
    h.update(b"\x00")
    h.update(extra.encode("utf-8"))
    h.update(b"\x00")
    h.update(system_prompt.encode("utf-8"))
    h.update(b"\x00")
    h.update(user_content.encode("utf-8"))
    return h.hexdigest()


def _llm_cache_get(model: str, key: str) -> Optional[Dict[str, Any]]:
    if _llm_cache_disabled():
        return None
    fp = _llm_cache_root() / model / f"{key}.json"
    try:
        with fp.open("r", encoding="utf-8") as fh:
            return json.load(fh)
    except FileNotFoundError:
        return None
    except (OSError, json.JSONDecodeError) as e:
        logger.warning("llm cache read failed for %s: %s", fp, e)
        return None


def _llm_cache_put(model: str, key: str, value: Dict[str, Any]) -> None:
    if _llm_cache_disabled():
        return
    fp = _llm_cache_root() / model / f"{key}.json"
    try:
        fp.parent.mkdir(parents=True, exist_ok=True)
        # Atomic-ish write via temp file in the same dir.
        tmp = fp.with_suffix(".json.tmp")
        with tmp.open("w", encoding="utf-8") as fh:
            json.dump(value, fh, ensure_ascii=False, sort_keys=True)
        tmp.replace(fp)
    except OSError as e:
        logger.warning("llm cache write failed for %s: %s", fp, e)


_llm_cache_hits = 0
_llm_cache_misses = 0
_llm_cache_stats_lock = threading.Lock()


def _llm_cache_record(hit: bool) -> None:
    global _llm_cache_hits, _llm_cache_misses
    with _llm_cache_stats_lock:
        if hit:
            _llm_cache_hits += 1
        else:
            _llm_cache_misses += 1


def _llm_cache_reset_stats() -> None:
    global _llm_cache_hits, _llm_cache_misses
    with _llm_cache_stats_lock:
        _llm_cache_hits = 0
        _llm_cache_misses = 0


def _llm_cache_get_stats() -> Tuple[int, int]:
    with _llm_cache_stats_lock:
        return _llm_cache_hits, _llm_cache_misses


