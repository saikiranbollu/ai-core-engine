def _expand_indexed(text: str, letter: str, value: int) -> str:
    """Replace the placeholder letter in *text* with the concrete *value*.

    Handles two cases in one pass:

    * Inside ALL-CAPS register names — a single lowercase letter sitting
      between uppercase / underscore boundaries (e.g. ``CXPI_CHx_CTL1``
      with ``letter='x'`` and ``value=0`` → ``CXPI_CH0_CTL1``).
    * Standalone in prose — the placeholder letter as its own word
      (e.g. ``"Channel x control"`` → ``"Channel 0 control"``).

    Also strips any inline ``(letter=N-M)`` annotation.
    """
    if not text or not letter:
        return text
    out = re.sub(
        rf"\s*\(\s*{re.escape(letter)}\s*=\s*\d+\s*"
        rf"(?:-|\u2013|\u2014|\.\.|\u2026|to)\s*\d+\s*\)",
        "", text,
    )
    out = re.sub(
        rf"(?<=[A-Z]){re.escape(letter)}(?=[A-Z_0-9]|$)",
        str(value), out,
    )
    out = re.sub(
        rf"(?<!\w){re.escape(letter)}(?!\w)",
        str(value), out,
    )
    return out


def _xf(text: str, letter: Optional[str], value: Optional[int]) -> str:
    """Apply :func:`_expand_indexed` conditionally (no-op when not indexed)."""
    if letter is None or value is None:
        return text
    return _expand_indexed(text, letter, value)


def _unescape_md_name(raw: str) -> str:
    """Strip backslash-escapes and surrounding parens from a register name."""
    name = re.sub(r"\s*\([^)]*\)\s*$", "", raw).strip()
    name = name.replace("\\_", "_").replace("\\", "")
    return name.strip()


