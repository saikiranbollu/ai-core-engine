_SEQUENCE_PROMPT = """\
You are extracting hardware programming sequences from an Infineon HW manual.

Return ONLY valid JSON (no markdown fences, no explanation) matching this schema:

{
  "sequences": [
    {
      "name": "<sequence name, e.g. 'CXPI normal mode transition'>",
      "use_case": "<one of Init|Configure|Tx|Rx|Shutdown|WakeUp|PowerDown|SafetyCheck|ErrorRecovery|Calibration|Other>",
      "description": "<one-sentence description, max 200 chars>",
      "source_section": "<original markdown heading text>",
      "steps": [
        {
          "order": <integer starting at 0>,
          "name": "<short step name, max 80 chars>",
          "action": "<verbatim step text from the manual, max 300 chars>",
          "step_type": "<AssertReset|ReleaseReset|EnableClock|WaitBit|Delay|WriteRegister|ReadRegister|Check|Configure|Other>",
          "touched_register": "<register short name, or null>",
          "touched_field": "<field name, or null>",
          "expected_value": "<\\"0\\" or \\"1\\" string, or null>",
          "timeout": "<e.g. '100 ns', '2 cycles', or null>"
        }
      ]
    }
  ]
}

Rules:
- Only extract sequences that have at least 2 concrete steps describing register/field writes, polls, or waits.
- Skip purely narrative descriptions (block diagrams, feature overviews, marketing prose).
- Order steps starting at 0 in the order they appear in the manual.
- Keep step "action" verbatim (do not paraphrase). Trim if longer than 300 chars.
- For "step_type": classify by the dominant verb \u2014 "WriteRegister" for "write/program/set/configure register/bit", "WaitBit" for "wait until bit==X / poll FLAG", "Delay" for "wait N ns/cycles", "Check" for verify/ensure, etc.
- If no qualifying sequences exist in the chunk, return {"sequences": []}.
"""


def _strip_register_section_bodies(
    content: str, sections: List[Dict[str, Any]]
) -> str:
    """Remove text that's already covered by per-register sections.

    Used to feed only the non-register prose (chapter intros, programming
    chapters, mode-transition descriptions) to the document-level sequence
    extractor.
    """
    if not sections:
        return content
    lines = content.split("\n")
    drop = [False] * len(lines)
    for sec in sections:
        body = sec.get("text", "")
        if not body:
            continue
        body_lines = body.split("\n")
        try:
            start = lines.index(body_lines[0])
        except ValueError:
            continue
        end = min(start + len(body_lines), len(lines))
        for j in range(start, end):
            drop[j] = True
    return "\n".join(l for j, l in enumerate(lines) if not drop[j])


def _clean_doc_text(text: str) -> str:
    """Strip repeated page headers, draft markers and table rows."""
    out: List[str] = []
    for line in text.split("\n"):
        stripped = line.strip()
        if not stripped:
            out.append(line)
            continue
        if stripped == "D R A F T" or stripped.startswith("D R A F T"):
            continue
        if re.match(r"^#{1,2}\s+CXPI HWA-FBcontainer", stripped):
            continue
        if re.match(r"^#{1,2}\s+Pages?\s+\d+", stripped):
            continue
        if stripped.startswith("|"):
            continue
        out.append(line)
    return "\n".join(out)


def _chunk_on_h2(text: str, max_chars: int = 12000) -> List[str]:
    """Split text on H2 boundaries, packing chunks up to *max_chars*."""
    if len(text) <= max_chars:
        return [text]
    lines = text.split("\n")
    blocks: List[List[str]] = []
    cur: List[str] = []
    for line in lines:
        if line.startswith("## ") and cur:
            blocks.append(cur)
            cur = [line]
        else:
            cur.append(line)
    if cur:
        blocks.append(cur)
    chunks: List[str] = []
    buf: List[str] = []
    buf_len = 0
    for block in blocks:
        block_text = "\n".join(block)
        if buf and buf_len + len(block_text) > max_chars:
            chunks.append("\n".join(buf))
            buf = [block_text]
            buf_len = len(block_text)
        else:
            buf.append(block_text)
            buf_len += len(block_text) + 1
    if buf:
        chunks.append("\n".join(buf))
    return chunks


_ALLOWED_USE_CASES = {
    "Init", "Configure", "Tx", "Rx", "Shutdown", "WakeUp",
    "PowerDown", "SafetyCheck", "ErrorRecovery", "Calibration", "Other",
}
_ALLOWED_STEP_TYPES = {
    "AssertReset", "ReleaseReset", "EnableClock", "WaitBit", "Delay",
    "WriteRegister", "ReadRegister", "Check", "Configure", "Other",
}


def _llm_call_sequence_chunk(
    client, model: str, chunk: str, retry: int = 0,
) -> Optional[Dict[str, Any]]:
    """Send one document chunk to the LLM and parse its sequence JSON reply.

    Cached on disk identically to :func:`_llm_extract_section` so repeated
    runs over the same MD are deterministic and avoid 429 rate-limits.
    """
    cache_key = _llm_cache_key(
        model=model,
        system_prompt=_SEQUENCE_PROMPT,
        user_content=chunk,
        seed=_LLM_SEED,
        extra="sequence-chunk",
    )
    cached = _llm_cache_get(model, cache_key)
    if cached is not None:
        _llm_cache_record(hit=True)
        return cached
    _llm_cache_record(hit=False)
    try:
        resp = client.chat.completions.create(
            model=model,
            temperature=0,
            seed=_LLM_SEED,
            max_completion_tokens=6000,
            messages=[
                {"role": "system", "content": _SEQUENCE_PROMPT},
                {"role": "user", "content": chunk},
            ],
        )
        raw = (resp.choices[0].message.content or "").strip()
        if raw.startswith("```"):
            raw = re.sub(r"^```(?:json)?\s*\n?", "", raw)
            raw = re.sub(r"\n?```\s*$", "", raw)
        parsed = json.loads(raw)
        _llm_cache_put(model, cache_key, parsed)
        return parsed
    except json.JSONDecodeError as e:
        if retry < _MAX_RETRIES - 1:
            logger.warning(
                "sequence JSON parse error (attempt %d): %s — retrying",
                retry + 1, e,
            )
            time.sleep(2)
            return _llm_call_sequence_chunk(client, model, chunk, retry + 1)
        logger.error("sequence LLM JSON parse failed after %d retries",
                     _MAX_RETRIES)
        return None
    except Exception as e:
        if retry < _MAX_RETRIES - 1:
            is_rate = "429" in str(e) or "Rate limit" in str(e)
            wait = 30 if is_rate else 5
            logger.warning(
                "sequence LLM call failed (attempt %d): %s — retrying in %ds",
                retry + 1, e, wait,
            )
            time.sleep(wait)
            return _llm_call_sequence_chunk(client, model, chunk, retry + 1)
        logger.error("sequence LLM extraction failed after %d retries: %s",
                     _MAX_RETRIES, e)
        return None


def _llm_extract_sequences(
    content: str,
    *,
    model: str = _DEFAULT_MODEL,
    max_workers: int = 2,
    api_key: Optional[str] = None,
    exclude_sections: Optional[List[Dict[str, Any]]] = None,
) -> List[Dict[str, Any]]:
    """Document/chapter-level LLM extraction of programming sequences.

    Strips the per-register section bodies (already covered by the section
    LLM pass) and runs a separate LLM call — chunked at H2 boundaries —
    over the remaining prose to recover sequences that live outside any
    register section (mode transitions, programming chapters, etc.).
    Returns ``[]`` when the LLM client is unavailable or no sequences are
    found.
    """
    doc_text = _strip_register_section_bodies(content, exclude_sections or [])
    doc_text = _clean_doc_text(doc_text)
    if not doc_text.strip():
        return []
    chunks = _chunk_on_h2(doc_text)
    logger.info("sequence LLM: %d chunk(s), %d total chars",
                len(chunks), len(doc_text))

    try:
        token = api_key or _llm_get_token()
        client = _llm_build_client(token, model)
    except Exception as e:
        logger.warning(
            "sequence LLM client unavailable (%s) — skipping doc-level pass", e,
        )
        return []

    rate_limiter = _RateLimiter(_RATE_LIMIT_RPM)
    results: List[Optional[Dict[str, Any]]] = [None] * len(chunks)
    _llm_cache_reset_stats()

    def _process(idx: int):
        rate_limiter.acquire()
        return idx, _llm_call_sequence_chunk(client, model, chunks[idx])

    t0 = time.time()
    with ThreadPoolExecutor(max_workers=max(1, max_workers)) as pool:
        futures = {pool.submit(_process, i): i for i in range(len(chunks))}
        for fut in as_completed(futures):
            idx, res = fut.result()
            results[idx] = res
    elapsed = time.time() - t0
    _seq_hits, _seq_misses = _llm_cache_get_stats()

    sequences: List[Dict[str, Any]] = []
    seen: set = set()
    for res in results:
        if not res:
            continue
        for seq in (res.get("sequences") or []):
            if not isinstance(seq, dict):
                continue
            name = (seq.get("name") or "").strip()
            if not name or name in seen:
                continue
            use_case = (seq.get("use_case") or "Other").strip()
            if use_case not in _ALLOWED_USE_CASES:
                use_case = "Other"
            raw_steps = seq.get("steps") or []
            steps: List[Dict[str, Any]] = []
            for s in raw_steps:
                if not isinstance(s, dict):
                    continue
                action = ((s.get("action") or "")[:300]).strip()
                if not action:
                    continue
                step_type = (s.get("step_type") or "Other").strip()
                if step_type not in _ALLOWED_STEP_TYPES:
                    step_type = "Other"
                order_raw = s.get("order")
                order = order_raw if isinstance(order_raw, int) else len(steps)
                expected_raw = s.get("expected_value")
                expected = (
                    str(expected_raw)
                    if expected_raw not in (None, "") else None
                )
                steps.append({
                    "order": order,
                    "name": ((s.get("name") or action)[:80]).strip(),
                    "action": action,
                    "step_type": step_type,
                    "touched_register": (s.get("touched_register") or None),
                    "touched_field": (s.get("touched_field") or None),
                    "expected_value": expected,
                    "timeout": (s.get("timeout") or None),
                })
            if len(steps) < 2:
                continue
            seen.add(name)
            sequences.append({
                "id": f"PSEQ_{re.sub(r'[^A-Za-z0-9_]+', '_', name)[:60]}",
                "name": name[:80],
                "use_case": use_case,
                "description": ((seq.get("description") or "")[:200]).strip(),
                "step_count": len(steps),
                "source_section":
                    ((seq.get("source_section") or name)[:120]).strip(),
                "steps": steps,
            })
    logger.info(
        "sequence LLM: %d sequences extracted in %.1fs "
        "(cache hits=%d, misses=%d)",
        len(sequences), elapsed, _seq_hits, _seq_misses,
    )
    return sequences


def _extract_interrupts_from_fields(
    fields: List[Dict[str, Any]],
) -> List[Dict[str, Any]]:
    """Derive interrupts from LLM-extracted fields (single-bit + INT/IRQ kw)."""
    out: List[Dict[str, Any]] = []
    seen: set = set()
    for f in fields:
        name_u = f["name"].upper()
        reg_u = f.get("parent_register", "").upper()
        desc_u = f.get("description", "").upper()
        is_int = (any(k in name_u for k in _INT_KWS_BROAD)
                  or any(k in reg_u for k in _INT_KWS_BROAD)
                  or "INTERRUPT" in desc_u
                  or "SERVICE REQUEST" in desc_u)
        if not is_int:
            continue
        bits = f.get("bits", "")
        if ":" in bits or f["name"] in seen:
            continue
        seen.add(f["name"])
        out.append({
            "name": f["name"],
            "register": f["parent_register"],
            "bit": int(bits) if bits.isdigit() else 0,
            "description": f.get("description", "")[:200],
        })
    return out


def _extract_errors_from_fields(
    fields: List[Dict[str, Any]],
) -> List[Dict[str, Any]]:
    """Derive errors from LLM-extracted fields by name keyword."""
    out: List[Dict[str, Any]] = []
    seen: set = set()
    for f in fields:
        name_u = f["name"].upper()
        if not any(k in name_u for k in _ERROR_KWS):
            continue
        if f["name"] in seen:
            continue
        seen.add(f["name"])
        if "TX" in name_u or "TRANSMIT" in name_u or "SEND" in name_u:
            etype = "transmission"
        elif "RX" in name_u or "RECEIVE" in name_u or "RECV" in name_u:
            etype = "reception"
        elif "BUS" in name_u or "ARB" in name_u:
            etype = "bus"
        elif "PARITY" in name_u or "CRC" in name_u or "CHECKSUM" in name_u:
            etype = "data_integrity"
        elif "TIMEOUT" in name_u or "WATCHDOG" in name_u:
            etype = "timing"
        else:
            etype = "general"
        bits = f.get("bits", "")
        out.append({
            "name": f["name"],
            "type": etype,
            "description": f.get("description", "")[:200],
            "detected_in": f["parent_register"],
            "bit": int(bits.split(":")[0]) if re.match(r"^\d+", bits) else None,
        })
    return out


