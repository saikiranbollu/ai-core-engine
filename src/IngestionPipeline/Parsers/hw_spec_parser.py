class _HWSpecExtractor:
    """Hybrid hardware-specification extractor (LLM + regex)."""

    def __init__(
        self,
        content: str,
        source_name: str,
        *,
        use_llm: bool = True,
        model: str = _DEFAULT_MODEL,
        max_workers: int = 3,
        api_key: Optional[str] = None,
    ):
        self._content = content
        self._lines = content.split("\n")
        self._source = source_name
        self._use_llm = use_llm
        self._model = model
        self._max_workers = max_workers
        self._api_key = api_key
        # Set to True after a successful LLM pass; controls downstream paths
        # for interrupts/errors (heading-based vs field-derived).
        self._llm_used = False

    # ── helpers ──────────────────────────────────────────────────────

    @staticmethod
    def _is_formatting_row(cells: List[str]) -> bool:
        for cell in cells:
            c = cell.strip()
            if not c:
                continue
            if re.match(r"^[:\-\s]+$", c):
                return True
        first = cells[0].strip().lower() if cells else ""
        return first in {"field", "name", "bit", "bits"}

    @staticmethod
    def _is_reserved(name: str) -> bool:
        c = name.strip()
        return c == "0" or c.lower() == "reserved"

    # ── 1. Registers ────────────────────────────────────────────────

    def extract_registers(self) -> List[Dict[str, Any]]:
        registers: List[Dict[str, Any]] = []
        seen: set[str] = set()
        pat1 = re.compile(r"^([A-Z][A-Za-z0-9_]+(?:\s*\([^)]+\))?)\s*[–—]\s*(.+)$")

        i = 0
        while i < len(self._lines):
            line = self._lines[i].strip()

            # Pattern 1: NAME — Description (same line)
            m = pat1.match(line)
            if m:
                short = re.sub(r"\s*\([^)]+\)", "", m.group(1)).strip()
                if short not in seen:
                    reg = self._scan_offset_reset(i, short, m.group(2).strip())
                    if reg:
                        seen.add(short)
                        registers.append(reg)
                i += 1
                continue

            # Pattern 2: NAME alone on a line, description on next line(s)
            if (
                re.match(r"^[A-Z][A-Za-z0-9_]+(?:\s*\([^)]+\))?\s*$", line)
                and len(line) <= 50
            ):
                short = re.sub(r"\s*\([^)]+\)", "", line).strip()
                if ("_" in short or short.isupper()) and short not in seen:
                    long_name = self._find_description(i)
                    if long_name:
                        reg = self._scan_offset_reset(i, short, long_name, lookahead=11)
                        if reg:
                            seen.add(short)
                            registers.append(reg)
            i += 1

        logger.info("Extracted %d registers", len(registers))
        return registers

    def _scan_offset_reset(
        self, start: int, short: str, long_name: str, lookahead: int = 6
    ) -> Dict[str, Any] | None:
        offset = reset_value = reset_type = None
        for j in range(start + 1, min(start + lookahead, len(self._lines))):
            ahead = self._lines[j].strip()
            if ahead.startswith("- "):
                ahead = ahead[2:].strip()
            if ahead.startswith("Offset address:"):
                offset = ahead.replace("Offset address:", "").strip()
            rm = re.match(r"(rst_\w+)\s+value:\s*(.+)", ahead)
            if rm:
                reset_type, reset_value = rm.group(1), rm.group(2).strip()
        if not offset:
            return None
        return {
            "name": short,
            "long_name": long_name,
            "offset": offset,
            "reset_value": reset_value,
            "reset_type": reset_type,
        }

    def _find_description(self, start: int) -> str | None:
        for idx in range(start + 1, min(start + 15, len(self._lines))):
            cl = self._lines[idx].strip()
            if not cl:
                continue
            raw = cl[2:].strip() if cl.startswith("- ") else cl
            if raw.startswith("Offset") or re.match(r"^rst_\w+", raw) or raw.startswith("|"):
                continue
            if re.match(r"^[A-Z][A-Za-z0-9_]+(?:\s*\([^)]+\))?\s*$", cl) and len(cl) <= 50:
                break
            if len(cl) > 10:
                return cl
        return None

    # ── 2. Fields ───────────────────────────────────────────────────

    def extract_fields(self, registers: List[Dict[str, Any]]) -> List[Dict[str, Any]]:
        reg_info = [
            {"name": r["name"], "long_name": r["long_name"]}
            for r in registers
        ]

        all_fields: List[Dict[str, Any]] = []
        i = 0
        while i < len(self._lines):
            line = self._lines[i]
            current_reg = self._match_register_context(line, i, reg_info)
            if current_reg:
                for j in range(i, min(i + 100, len(self._lines))):
                    tl = self._lines[j]
                    if tl.strip().startswith("|") and tl.count("|") >= 4:
                        field = self._parse_field_row(tl, current_reg)
                        if field:
                            all_fields.append(field)
            i += 1

        # Deduplicate
        seen: set[tuple] = set()
        unique: List[Dict[str, Any]] = []
        for f in all_fields:
            key = (f["parent_register"], f["name"], f["bits"])
            if key not in seen:
                seen.add(key)
                unique.append(f)

        logger.info("Extracted %d unique fields", len(unique))
        return unique

    def _match_register_context(
        self, line: str, idx: int, reg_info: List[Dict]
    ) -> str | None:
        for ri in reg_info:
            if ri["name"] in line or ri["long_name"] in line:
                if "\u2014" in line or "\u2013" in line:
                    return ri["name"]
                for ci in range(idx + 1, min(idx + 5, len(self._lines))):
                    if "offset address" in self._lines[ci].lower():
                        return ri["name"]
        return None

    def _parse_field_row(self, line: str, register: str) -> Dict[str, Any] | None:
        cells = [c.strip() for c in line.split("|")[1:-1]]
        if len(cells) < 3 or self._is_formatting_row(cells):
            return None
        name = cells[0].strip()
        bits = cells[1].strip() if len(cells) > 1 else ""
        ftype = cells[2].strip() if len(cells) > 2 else ""
        desc = cells[3].strip() if len(cells) > 3 else ""
        if self._is_reserved(name) or not name or not bits:
            return None
        if not re.match(r"^\d+(:\d+)?$", bits):
            return None
        if ftype and not re.match(r"^r[wh]?|w$", ftype):
            return None
        return {
            "name": name,
            "parent_register": register,
            "bits": bits,
            "type": ftype,
            "description": desc[:200],
        }

    # ── 3. Interrupts ──────────────────────────────────────────────

    def extract_interrupts(self) -> List[Dict[str, Any]]:
        sections = []
        for i, line in enumerate(self._lines):
            if "\u2014" not in line:
                continue
            parts = line.split("\u2014")
            if len(parts) < 2:
                continue
            reg_name = re.sub(r"\s*\([^)]+\)", "", parts[0].strip()).strip()
            desc = parts[1].strip().lower()
            if any(kw in reg_name.upper() for kw in _INTERRUPT_KWS) or "interrupt" in desc:
                sections.append({"register": reg_name, "line": i})

        interrupts: List[Dict[str, Any]] = []
        for sec in sections:
            for j in range(sec["line"], min(sec["line"] + 150, len(self._lines))):
                tl = self._lines[j]
                if not (tl.strip().startswith("|") and tl.count("|") >= 4):
                    continue
                cells = [c.strip() for c in tl.split("|")[1:-1]]
                if len(cells) < 3 or self._is_formatting_row(cells):
                    continue
                name = cells[0].strip()
                bits = cells[1].strip() if len(cells) > 1 else ""
                desc = cells[3].strip() if len(cells) > 3 else ""
                if self._is_reserved(name) or not name or not bits:
                    continue
                if not re.match(r"^\d+$", bits):
                    continue
                interrupts.append({
                    "name": name,
                    "register": sec["register"],
                    "bit": int(bits),
                    "description": desc[:200],
                })

        logger.info("Extracted %d interrupts", len(interrupts))
        return interrupts

    # ── 4. Errors ───────────────────────────────────────────────────

    def extract_errors(self) -> List[Dict[str, Any]]:
        errors: List[Dict[str, Any]] = []
        seen: set[str] = set()
        current_reg: str | None = None

        for line in self._lines:
            if "\u2014" in line and not line.strip().startswith("|"):
                m = re.match(r"^([A-Z][A-Za-z0-9_]+)", line.split("\u2014")[0].strip())
                if m:
                    current_reg = m.group(1)

            if not current_reg or not (line.strip().startswith("|") and line.count("|") >= 4):
                continue
            cells = [c.strip() for c in line.split("|")[1:-1]]
            if len(cells) < 3 or self._is_formatting_row(cells):
                continue
            name = cells[0].strip()
            bits = cells[1].strip() if len(cells) > 1 else ""
            desc = cells[3].strip() if len(cells) > 3 else ""
            if not any(kw in name.upper() for kw in _ERROR_KWS):
                continue
            if not re.match(r"^\d+(:\d+)?$", bits) or name in seen:
                continue
            seen.add(name)

            upper = name.upper()
            if "TX" in upper or "TRANSMIT" in upper or "SEND" in upper:
                etype = "transmission"
            elif "RX" in upper or "RECEIVE" in upper or "RECV" in upper:
                etype = "reception"
            elif "BUS" in upper or "ARB" in upper:
                etype = "bus"
            elif "PARITY" in upper or "CRC" in upper or "CHECKSUM" in upper:
                etype = "data_integrity"
            elif "TIMEOUT" in upper or "WATCHDOG" in upper:
                etype = "timing"
            else:
                etype = "general"

            errors.append({
                "name": name,
                "type": etype,
                "description": desc[:200],
                "detected_in": current_reg,
                "bit": int(bits.split(":")[0]) if re.match(r"^\d+", bits) else None,
            })

        logger.info("Extracted %d errors", len(errors))
        return errors

    # ── 5. Formulas ─────────────────────────────────────────────────

    def extract_formulas(self) -> List[Dict[str, Any]]:
        formulas: List[Dict[str, Any]] = []
        seen: set[str] = set()

        def _add(text: str) -> None:
            text = text.strip()
            if text in seen:
                return
            if re.match(r"^[A-Z_]+\s*=\s*\d+$", text):
                return
            if text.endswith("= (") or text.endswith("=("):
                return
            seen.add(text)
            formulas.append({
                "name": f"formula_{len(formulas) + 1}",
                "formula": text,
                "type": "calculation",
                "description": "Mathematical formula from hardware manual",
            })

        # Method 1: LaTeX $$...$$ blocks
        for m in re.finditer(r"\$\$(.+?)\$\$", self._content, re.DOTALL):
            _add(m.group(1))

        # Method 2: plain-text equations with math operators
        eq_pat = re.compile(
            r"^[\s\-•]*([A-Za-z_][A-Za-z0-9_,\.]*)\s*=\s*(.+?)(?:\s*\[Equation\s+\d+\])?$"
        )
        for line in self._lines:
            m = eq_pat.match(line.strip())
            if m:
                expr = m.group(2).strip()
                if any(op in expr for op in ["/", "*", "+", "-", "\u00d7", "\u00f7", "(", ")"]):
                    if not re.match(r"^\d+(\.\d+)?$", expr):
                        _add(f"{m.group(1)} = {expr}")

        # Method 3: labeled equations [Equation N]
        lbl = re.compile(r"\[Equation\s+\d+\]")
        for line in self._lines:
            if lbl.search(line):
                part = lbl.split(line)[0].strip()
                if part and "=" in part:
                    part = re.sub(r"^[\s\-•]+", "", part)
                    _add(part)

        logger.info("Extracted %d formulas", len(formulas))
        return formulas

    # ── 6a. HW Constraints (v3.0) ────────────────────────────────────

    def extract_hw_constraints(
        self,
        registers: List[Dict[str, Any]],
        fields: List[Dict[str, Any]],
    ) -> List[Dict[str, Any]]:
        """
        Mine the HW manual for timing / wait-loop / ordering / access-width /
        frequency constraints.  Each constraint is attached to a register,
        field, or operation by name.  Returns a list of dicts ready for the
        KG builder to create :HwConstraint nodes + :HAS_CONSTRAINT edges.
        """
        # Build register/field name → lookup for context resolution
        reg_names = {r["name"] for r in registers}
        field_index: Dict[str, Dict[str, Any]] = {}
        for f in fields:
            field_index.setdefault(f["name"].upper(), f)

        constraints: List[Dict[str, Any]] = []
        seen: set[tuple] = set()
        current_reg: str | None = None
        section_title: str | None = None

        def _emit(kind: str, text: str, **extra) -> None:
            target = extra.pop("target", None) or {"label": "HardwareRegister", "name": current_reg}
            if not target.get("name"):
                return
            key = (kind, target.get("label"), target.get("name"),
                   extra.get("value"), text[:80])
            if key in seen:
                return
            seen.add(key)
            constraints.append({
                "kind": kind,
                "name": f"{target['name']}_{kind}_{len(constraints)+1}",
                "target_label": target["label"],
                "target_name": target["name"],
                "target_field": target.get("field"),
                "condition": text.strip()[:300],
                "source_section": section_title,
                **extra,
            })

        for i, line in enumerate(self._lines):
            stripped = line.strip()

            # Track current register context via "NAME — Description" headings
            if "\u2014" in stripped:
                head = stripped.split("\u2014")[0].strip()
                m = re.match(r"^([A-Z][A-Za-z0-9_]+)", head)
                if m and m.group(1) in reg_names:
                    current_reg = m.group(1)

            # Track Markdown section heading (used as source_section citation)
            if stripped.startswith("#"):
                section_title = stripped.lstrip("# ").strip()[:120]

            lower = stripped.lower()
            if not stripped or stripped.startswith("|"):
                continue

            # 1. Wait / delay
            for wm in _WAIT_RE.finditer(stripped):
                vm = _VALUE_UNIT_RE.search(wm.group(0))
                value = vm.group("value") if vm else None
                unit = vm.group("unit") if vm else None
                _emit(
                    kind="timing",
                    text=wm.group(0),
                    value=value,
                    unit=unit,
                )

            # 2. Poll-until-bit
            for pm in _POLL_BIT_RE.finditer(stripped):
                fld = pm.group("field").upper()
                val = pm.group("val").lower()
                expected = "0" if val in ("0", "cleared") else "1"
                target = None
                fi = field_index.get(fld)
                if fi:
                    target = {
                        "label": "RegisterField",
                        "name": fi["name"],
                        "field": fi["name"],
                    }
                _emit(
                    kind="wait_loop",
                    text=pm.group(0),
                    value=expected,
                    unit="bit_value",
                    target=target,
                )

            # 3. Ordering (must be set before / after …)
            for om in _ORDER_RE.finditer(stripped):
                _emit(kind="ordering", text=om.group(0))

            # 4. Access width
            for am in _ACCESS_WIDTH_RE.finditer(stripped):
                width = am.groupdict().get("width")
                _emit(
                    kind="access_width",
                    text=am.group(0),
                    value=width,
                    unit="bits" if width else None,
                )

            # 5. Frequency
            for fm in _FREQ_RE.finditer(stripped):
                vm = _VALUE_UNIT_RE.search(fm.group(0))
                _emit(
                    kind="frequency",
                    text=fm.group(0),
                    value=vm.group("value") if vm else None,
                    unit=vm.group("unit") if vm else None,
                )

            # 6. Side-effect: read-clears, write-1-to-clear, sticky
            if re.search(r"\b(?:read[-\s]?clears?|cleared\s+on\s+read|sticky"
                         r"|write[-\s]?one[-\s]?to[-\s]?clear|w1c|rc)\b", lower):
                _emit(kind="side_effect", text=stripped[:200])

        logger.info("Extracted %d hw constraints", len(constraints))
        return constraints

    # ── 6b. Programming Sequences (v3.0) ─────────────────────────────

    def extract_programming_sequences(
        self,
        registers: List[Dict[str, Any]],
        fields: List[Dict[str, Any]],
    ) -> List[Dict[str, Any]]:
        """
        Detect named programming sequences (Init / Tx / Rx / Shutdown ...)
        from Markdown headings and extract their ordered steps.
        """
        reg_names = {r["name"] for r in registers}
        field_names = {f["name"]: f["parent_register"] for f in fields}

        sequences: List[Dict[str, Any]] = []

        i = 0
        n = len(self._lines)
        while i < n:
            line = self._lines[i].rstrip()
            heading_name = self._match_sequence_heading(line)
            if heading_name is None:
                i += 1
                continue

            # Slurp the body until the next H#-or-lower heading or 80 lines
            heading_level = len(re.match(r"^#+", line).group(0)) if line.startswith("#") else 0
            body_lines: List[str] = []
            j = i + 1
            while j < n and (j - i) < 200:
                nxt = self._lines[j].rstrip()
                if nxt.startswith("#"):
                    nxt_level = len(re.match(r"^#+", nxt).group(0))
                    if nxt_level <= heading_level:
                        break
                body_lines.append(nxt)
                j += 1

            steps = self._parse_sequence_steps(body_lines, reg_names, field_names)
            if steps:
                use_case = self._sequence_use_case(heading_name)
                seq_id = f"PSEQ_{heading_name.strip().replace(' ', '_')}"
                sequences.append({
                    "id": seq_id,
                    "name": heading_name.strip()[:80],
                    "use_case": use_case,
                    "description": line.lstrip("# ").strip()[:200],
                    "step_count": len(steps),
                    "source_section": line.lstrip("# ").strip()[:120],
                    "steps": steps,
                })
            i = j

        logger.info("Extracted %d programming sequences", len(sequences))
        return sequences

    @staticmethod
    def _match_sequence_heading(line: str) -> str | None:
        for pat in _SEQUENCE_HEADING_PATTERNS:
            m = pat.match(line)
            if m:
                return m.group("name")
        return None

    @staticmethod
    def _sequence_use_case(heading_name: str) -> str:
        key = heading_name.strip().lower()
        if key in _USE_CASE_MAP:
            return _USE_CASE_MAP[key]
        for k, v in _USE_CASE_MAP.items():
            if k in key:
                return v
        return "Other"

    @staticmethod
    def _classify_step(action: str) -> str:
        for rx, t in _STEP_TYPE_RULES:
            if rx.search(action):
                return t
        return "Other"

    def _parse_sequence_steps(
        self,
        body_lines: List[str],
        reg_names: set[str],
        field_names: Dict[str, str],
    ) -> List[Dict[str, Any]]:
        steps: List[Dict[str, Any]] = []
        order = 0
        for raw in body_lines:
            sm = _STEP_NUM_RE.match(raw)
            if not sm:
                continue
            action = sm.group("body").strip()
            if len(action) < 4:
                continue

            step_type = self._classify_step(action)

            # Resolve register/field touched by this step
            touched_register: str | None = None
            touched_field: str | None = None
            for tok in re.findall(r"\b([A-Z][A-Z0-9_]{2,})\b", action):
                if tok in reg_names:
                    touched_register = tok
                    break
                if tok in field_names:
                    touched_field = tok
                    touched_register = field_names[tok]
                    break

            # Pull expected_value for wait/check steps
            expected_value = None
            pm = _POLL_BIT_RE.search(action)
            if pm:
                expected_value = "0" if pm.group("val").lower() in ("0", "cleared") else "1"
                if not touched_field:
                    touched_field = pm.group("field").upper()
                    touched_register = field_names.get(touched_field, touched_register)

            # Pull timeout text from Delay/WaitBit
            timeout = None
            if step_type in ("WaitBit", "Delay"):
                tm = _VALUE_UNIT_RE.search(action)
                if tm:
                    timeout = f"{tm.group('value')} {tm.group('unit')}"

            steps.append({
                "order": order,
                "name": action[:80],
                "action": action[:300],
                "step_type": step_type,
                "touched_register": touched_register,
                "touched_field": touched_field,
                "expected_value": expected_value,
                "timeout": timeout,
            })
            order += 1
        return steps

    # ── 6c. Hardware sub-modules (v3.0) ──────────────────────────────

    def extract_submodules(self) -> List[Dict[str, Any]]:
        """
        Detect HW sub-module instances (e.g. CXPI0, CXPI1, SPI3) and their
        base addresses (if mentioned in the manual).
        """
        seen: Dict[str, Dict[str, Any]] = {}
        # Pass 1: find names
        for line in self._lines:
            for m in _SUBMODULE_RE.finditer(line):
                name = m.group("name")
                if not re.match(r"^[A-Z][A-Z0-9]{1,8}\d+$", name):
                    continue
                seen.setdefault(name, {"name": name, "base_address": None,
                                       "description": ""})
        # Pass 2: look for base-address declarations near a sub-module name
        addr_re = re.compile(
            r"\b(?P<name>[A-Z][A-Z0-9]{1,8}\d+)\b[^.;:]{0,40}?"
            r"(?:base\s+address|@|at)\s*[:=]?\s*(?P<addr>0x[0-9A-Fa-f]+)",
            re.IGNORECASE,
        )
        for line in self._lines:
            am = addr_re.search(line)
            if am:
                sub = seen.get(am.group("name"))
                if sub and not sub["base_address"]:
                    sub["base_address"] = am.group("addr")

        submodules = list(seen.values())
        # Heuristic: keep only sub-modules that look like real instances
        # (must contain a digit at the end and an alpha prefix).
        submodules = [
            s for s in submodules
            if re.match(r"^[A-Z]{2,}\d+$", s["name"])
        ]
        logger.info("Extracted %d hardware sub-modules", len(submodules))
        return submodules

    # ── 6. Relationships ────────────────────────────────────────────

    def build_relationships(
        self,
        registers: List[Dict],
        fields: List[Dict],
        interrupts: List[Dict],
        errors: List[Dict],
        formulas: List[Dict],
    ) -> Dict[str, List[Dict]]:
        rels: Dict[str, List[Dict]] = {
            "register_has_field": [],
            "error_sets_interrupt": [],
            "field_used_in_formula": [],
            "field_enables_feature": [],
            "interrupt_masked_by_field": [],
            "operation_triggers_interrupt": [],
        }

        # 1. register_has_field
        for f in fields:
            rels["register_has_field"].append({
                "register": f["parent_register"],
                "field": f["name"],
                "bits": f["bits"],
            })

        # 2. error_sets_interrupt
        seen_ei: set[tuple] = set()
        for err in errors:
            for intr in interrupts:
                if intr["name"] == err["name"]:
                    key = (err["name"], intr["name"], intr["register"], intr["bit"])
                    if key not in seen_ei:
                        seen_ei.add(key)
                        rels["error_sets_interrupt"].append({
                            "error": err["name"],
                            "error_register": err["detected_in"],
                            "interrupt": f"{intr['register']}.{intr['name']}",
                            "interrupt_register": intr["register"],
                            "interrupt_bit": intr["bit"],
                        })

        # 3. field_used_in_formula
        rels["field_used_in_formula"] = self._field_formula_rels(fields, formulas)

        # 4. field_enables_feature
        rels["field_enables_feature"] = self._field_enables_rels(fields)

        # 5. interrupt_masked_by_field (stub)
        # Will be implemented when mask-register patterns are finalised.

        # 6. operation_triggers_interrupt
        rels["operation_triggers_interrupt"] = self._op_trigger_rels(interrupts)

        for k, v in rels.items():
            logger.info("  %s: %d", k, len(v))
        return rels

    # ── relationship helpers ─────────────────────────────────────────

    def _field_formula_rels(
        self, fields: List[Dict], formulas: List[Dict]
    ) -> List[Dict]:
        lookup: Dict[str, Dict] = {}
        for f in fields:
            lookup[f["name"]] = f
            lookup[f["name"].replace("_", "")] = f

        rels: List[Dict] = []
        seen: set[tuple] = set()
        for formula in formulas:
            clean = (
                formula["formula"]
                .replace("\\_", "_")
                .replace("\\times", "*")
                .replace("\\text{", "")
                .replace("\\,", " ")
                .replace("\\quad", " ")
            )
            for potential in re.findall(
                r"[A-Z][A-Za-z0-9_]*(?:\.[A-Z][A-Za-z0-9_]*)?", clean
            ):
                fname = potential.split(".")[-1] if "." in potential else potential
                if fname not in lookup:
                    continue
                fi = lookup[fname]
                role = (
                    "output"
                    if re.search(rf"^{re.escape(fname)}\s*=", clean)
                    else "input"
                )
                key = (formula["name"], fname, fi["parent_register"])
                if key not in seen:
                    seen.add(key)
                    rels.append({
                        "formula": formula["name"],
                        "field": fname,
                        "register": fi["parent_register"],
                        "bits": fi["bits"],
                        "role": role,
                        "formula_content": formula["formula"][:100],
                    })
        return rels

    def _field_enables_rels(self, fields: List[Dict]) -> List[Dict]:
        rels: List[Dict] = []
        seen: set[tuple] = set()
        for f in fields:
            desc = f.get("description", "").lower()
            if not any(kw in desc for kw in _ENABLE_KWS):
                continue
            feature = None
            m = re.search(r"enable\s+(?:the\s+)?([a-z_][a-z0-9_\s]*)", desc)
            if m:
                feature = m.group(1).strip().replace(" ", "_")
            if not feature:
                m = re.search(r"([a-z_][a-z0-9_\s]*)\s+enable", desc)
                if m:
                    feature = m.group(1).strip().replace(" ", "_")
            if not feature:
                feature = (
                    re.sub(r"(_EN|_ENABLE|_ENABLED|EN)$", "", f["name"], flags=re.I).lower()
                    + "_feature"
                )
            key = (f["name"], f["parent_register"], feature)
            if key not in seen:
                seen.add(key)
                rels.append({
                    "field": f["name"],
                    "register": f["parent_register"],
                    "bits": f["bits"],
                    "feature": feature,
                    "enable_value": 1,
                    "disable_value": 0,
                    "description": f.get("description", "")[:100],
                })
        return rels

    def _op_trigger_rels(self, interrupts: List[Dict]) -> List[Dict]:
        rels: List[Dict] = []
        seen: set[tuple] = set()
        for intr in interrupts:
            name = intr["name"]
            desc = intr.get("description", "").lower()
            operation = self._detect_operation(name, desc)
            timing = self._detect_timing(name, desc)
            if operation:
                key = (operation, name)
                if key not in seen:
                    seen.add(key)
                    rels.append({
                        "operation": operation,
                        "interrupt": name,
                        "interrupt_register": intr["register"],
                        "interrupt_bit": intr["bit"],
                        "timing": timing,
                        "description": intr.get("description", "")[:100],
                    })
        return rels

    @staticmethod
    def _detect_operation(name: str, desc: str) -> str | None:
        # From description
        for tkw in _TIMING_KWS:
            if tkw in desc:
                remaining = desc.split(tkw, 1)[1]
                for ok, on in _OP_KWS.items():
                    if ok in remaining:
                        return on
        # From interrupt name
        upper = name.upper()
        if "TX" in upper or "TRANSMIT" in upper:
            return "transmit"
        if "RX" in upper or "RECEIVE" in upper:
            return "receive"
        if "SEND" in upper:
            return "send"
        # Generic: strip status words
        parts = name.lower().split("_")
        op_parts = [p for p in parts if p not in _STATUS_WORDS]
        return "_".join(op_parts) if op_parts else None

    @staticmethod
    def _detect_timing(name: str, desc: str) -> str:
        if "after" in desc or "complete" in desc or "done" in name.lower():
            return "after_completion"
        if "before" in desc or "start" in desc:
            return "before_start"
        if "during" in desc:
            return "during_operation"
        if "error" in name.lower() or "error" in desc:
            return "on_error"
        return "on_event"

    # ── top-level ────────────────────────────────────────────────────

    def _extract_via_llm_or_regex(self):
        """Run the LLM backend if enabled, falling back to regex on failure.

        Returns a 6-tuple ``(registers, fields, llm_interrupts, llm_errors,
        llm_constraints, clean_text)``.  When the regex fallback is used,
        the last four elements are empty.
        """
        if self._use_llm:
            regs, flds, ints, errs, cons, clean = _llm_extract_all(
                self._content,
                model=self._model,
                max_workers=self._max_workers,
                api_key=self._api_key,
            )
            if regs:
                self._llm_used = True
                logger.info(
                    "hw_spec_parser: LLM backend used — %d registers, %d fields, "
                    "%d interrupts, %d errors, %d constraints, %d clean-prose chars",
                    len(regs), len(flds), len(ints), len(errs), len(cons), len(clean),
                )
                return regs, flds, ints, errs, cons, clean
            logger.info(
                "hw_spec_parser: LLM produced no registers — falling back to regex"
            )
        regs = self.extract_registers()
        flds = self.extract_fields(regs)
        return regs, flds, [], [], [], ""

    def _run_with_content(self, alt_content: str, fn, *args, **kwargs):
        """Temporarily swap ``self._content``/``self._lines`` while running fn.

        Used to run regex extractors against the LLM-cleaned prose instead
        of the raw PDF→MD content (which is full of page-headers and broken
        tables).
        """
        saved_content, saved_lines = self._content, self._lines
        try:
            self._content = alt_content
            self._lines = alt_content.split("\n")
            return fn(*args, **kwargs)
        finally:
            self._content = saved_content
            self._lines = saved_lines

    @staticmethod
    def _merge_unique(primary: List[Dict[str, Any]],
                      secondary: List[Dict[str, Any]],
                      key_fn) -> List[Dict[str, Any]]:
        """Append items from secondary whose key is not already in primary."""
        seen = {key_fn(x) for x in primary}
        out = list(primary)
        for item in secondary:
            k = key_fn(item)
            if k not in seen:
                seen.add(k)
                out.append(item)
        return out

    def extract_all(self) -> Dict[str, Any]:
        (registers, fields, llm_interrupts, llm_errors,
         llm_constraints, clean_text) = self._extract_via_llm_or_regex()

        if self._llm_used:
            # LLM is primary source for interrupts/errors; field-keyword
            # derivation is a safety net for items the LLM might miss.
            derived_int = _extract_interrupts_from_fields(fields)
            derived_err = _extract_errors_from_fields(fields)
            interrupts = self._merge_unique(
                llm_interrupts, derived_int, key_fn=lambda x: x["name"],
            )
            errors = self._merge_unique(
                llm_errors, derived_err, key_fn=lambda x: x["name"],
            )
        else:
            interrupts = self.extract_interrupts()
            errors = self.extract_errors()

        formulas = self.extract_formulas()

        # v3.0 constraints/sequences/submodules.
        # When LLM was used, run the regex constraint/sequence extractors
        # against the LLM-cleaned prose (much better signal-to-noise than
        # the raw PDF→MD), then merge with the LLM-extracted constraints.
        if self._llm_used and clean_text:
            regex_constraints = self._run_with_content(
                clean_text, self.extract_hw_constraints, registers, fields,
            )
            constraints = self._merge_unique(
                llm_constraints, regex_constraints,
                key_fn=lambda c: (c["kind"], c["target_label"],
                                  c["target_name"], c.get("value"),
                                  c["condition"][:80]),
            )
            regex_sequences = self._run_with_content(
                clean_text, self.extract_programming_sequences,
                registers, fields,
            )
            # Document/chapter-level LLM pass for sequences outside
            # any register section (mode transitions, prog. chapters).
            try:
                doc_sections = _split_fbcontent_sections(self._content)
            except Exception:  # pragma: no cover - defensive
                doc_sections = []
            llm_sequences = _llm_extract_sequences(
                self._content,
                model=self._model,
                max_workers=2,
                api_key=self._api_key,
                exclude_sections=doc_sections,
            )
            sequences = self._merge_unique(
                llm_sequences, regex_sequences,
                key_fn=lambda s: (s.get("name") or "").strip().lower(),
            )
            # Sub-modules: run against raw content (the LLM-cleaned prose is
            # per-register only and rarely mentions instance names like
            # CXPI0/SPI3 that show up in chapter intros / address maps).
            submodules = self.extract_submodules()
        else:
            constraints = self.extract_hw_constraints(registers, fields)
            sequences = self.extract_programming_sequences(registers, fields)
            submodules = self.extract_submodules()
        relationships = self.build_relationships(
            registers, fields, interrupts, errors, formulas,
        )
        return {
            "metadata": {
                "source_file": self._source,
                "total_lines": len(self._lines),
                "extraction_date": datetime.now().isoformat(),
                "backend": "llm" if self._llm_used else "regex",
                "model": self._model if self._llm_used else None,
                "counts": {
                    "registers": len(registers),
                    "fields": len(fields),
                    "interrupts": len(interrupts),
                    "errors": len(errors),
                    "formulas": len(formulas),
                    "hw_constraints": len(constraints),
                    "programming_sequences": len(sequences),
                    "submodules": len(submodules),
                },
            },
            "registers": registers,
            "fields": fields,
            "interrupts": interrupts,
            "errors": errors,
            "formulas": formulas,
            "hw_constraints": constraints,
            "programming_sequences": sequences,
            "submodules": submodules,
            "relationships": relationships,
        }


