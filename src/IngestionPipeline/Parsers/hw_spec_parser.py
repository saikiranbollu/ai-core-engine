#     **Interrupt overview register**
#
#     - **Offset address:** 1004_H
#
# These are multi-word, may start with uppercase or title-case, and always
# have "offset address" in the near vicinity. We use them as section markers
# when the short-name pattern does not fire.
_BOLD_DESCRIPTIVE_RE = re.compile(
    r"^\s*\*\*\s*(?P<desc>[A-Z][A-Za-z0-9 _/\-]+(?:\s*[a-zx]\s*)?(?:\s*\([^)]*\))?)\s*\*\*\s*$"
)

# Reject descriptive bold markers that are clearly NOT register names but
# common prose/structural elements.
_DESCRIPTIVE_REJECT = re.compile(
    r"(?i)^(?:contents?|note|warning|caution|important|reserved|"
    r"figure|table|diagram|example|summary|overview|glossary|"
    r"register bit layout|field description|what )",
)


def _split_fbcontent_sections(content: str) -> List[Dict[str, str]]:
    """Split markdown into candidate register sections.

    FB-content layout produced by PyMuPDF for RC1 iLLD HW manuals comes in
    **two flavours**:

    1. **Heading-style** (legacy, 2025-10 manuals)::

           ## 1.7.9 Reset control register B
           ## CXPI\\_RST\\_CTRLB
           Offset address:
           00010H

    2. **Bold-paragraph style** (RC1 2026-04 manuals)::

           **CXPI_RST_CTRLA**
           Reset control register A

           Offset address: `0000C_H`

    We collect candidate register-start markers from both flavours and walk
    the document once, slicing each body up to the next marker (or the next
    markdown heading, or +400 lines). A section is accepted only when its
    body contains ``Offset address`` (excludes TOC / overview-table entries
    that mention the name in passing).
    """
    lines = content.split("\n")
    n = len(lines)

    headings: List[Tuple[int, int, str]] = []  # (line_idx, depth, raw_text)
    for i, raw in enumerate(lines):
        mh = _ANY_HEADING_RE.match(raw)
        if mh:
            headings.append((i, len(mh.group(1)), mh.group(2).strip()))

    # ── Bold-paragraph register markers (flavour 2) ───────────────────────
    # Each entry: (line_idx, name, desc_or_None). Only kept when "Offset
    # address" appears within 12 lines downstream — this avoids matching
    # **CONSTANT** or other accidental bold tokens.
    bold_markers: List[Tuple[int, str, str]] = []
    for i, raw in enumerate(lines):
        bm = _BOLD_NAME_RE.match(raw)
        if not bm:
            continue
        nm = _unescape_md_name(bm.group("name"))
        if not re.match(r"^[A-Z][A-Za-z0-9_]+$", nm):
            continue
        # Reject English words (contain 2+ consecutive lowercase letters).
        # Register names are abbreviations: all-caps with optional single
        # lowercase index placeholder (e.g. "CLC", "OCS", "INPx", "CPDRx").
        # English words like "Contents", "Reserved", "Figure" have runs of
        # lowercase letters and should be rejected.
        if re.search(r"[a-z]{2}", nm):
            continue
        lookahead = "\n".join(lines[i + 1 : i + 13])
        if "offset address" not in lookahead.lower():
            continue
        bold_markers.append((i, nm, (bm.group("desc") or bm.group("desc2") or "").strip()))

    # ── Bold-descriptive register markers (flavour 3) ─────────────────────
    # Some FBcontent manuals use the full register description as the bold
    # marker instead of the short name, e.g.:
    #     **Interrupt overview register**
    #     - **Offset address:** 1004_H
    # Detect these and use the descriptive text as a placeholder name; the
    # LLM will extract the correct short name from the section content.
    desc_bold_markers: List[Tuple[int, str, str]] = []
    bold_lines_already = {b[0] for b in bold_markers}
    for i, raw in enumerate(lines):
        if i in bold_lines_already:
            continue
        dm = _BOLD_DESCRIPTIVE_RE.match(raw)
        if not dm:
            continue
        desc_text = dm.group("desc").strip()
        # Must be multi-word (single uppercase words handled by flavour 2)
        if " " not in desc_text:
            continue
        # Reject structural/prose bold markers
        if _DESCRIPTIVE_REJECT.match(desc_text):
            continue
        # Must have "offset address" nearby
        lookahead = "\n".join(lines[i + 1 : i + 13])
        if "offset address" not in lookahead.lower():
            continue
        # Build a placeholder name from the descriptive text (uppercase, no
        # spaces). The LLM will override this with the real short name.
        placeholder = re.sub(r"[^A-Za-z0-9]", "_", desc_text).upper()[:40]
        desc_bold_markers.append((i, placeholder, desc_text))

    # ── Combined section slicer ───────────────────────────────────────────
    # The "boundary" for any section body is the line index of either the
    # next register-start marker (heading OR bold) or the next H1/H2/H3
    # heading — whichever comes first. A +400-line cap is kept as a safety
    # net.
    #
    # Important: in the RC1 2026-04 flavour, register bodies contain
    # auxiliary sub-headings such as ``## Diagrams / Figures``,
    # ``## Pages 73-74``, ``### Figure: ...`` and ``### Register bit-field
    # diagram: ...``. These are not the start of a new register section —
    # they are content-organisational headings INSIDE the current register.
    # If we treat them as boundaries, we slice the body too early and lose
    # later field tables (e.g. CXPI_CLC's second ``| DISS | EDIS | Reserved |``
    # table that follows the diagram explanation). Filter them out here.
    _NON_REGISTER_HEADING_RE = re.compile(
        r"^\s*(?:"
        r"diagrams?\s*/\s*figures?"      # "Diagrams / Figures"
        r"|pages?\s+\d+"                  # "Pages 73", "Pages 73-74"
        r"|figure\s*[:\.]"               # "Figure: ..."
        r"|diagram\s+explanation"
        r"|register\s+bit[-\s]?field\s+diagram"
        r"|register\s+(?:bit\s+)?layout" # "Register layout", "Register bit layout"
        r"|field\s+description(?:\s+table)?" # "Field description", "Field description table"
        r"|divider\s+factor"             # "Divider Factor of Pre Divider ..."
        r")",
        re.IGNORECASE,
    )
    heading_line_set = {
        h[0] for h in headings
        if h[1] <= 3 and not _NON_REGISTER_HEADING_RE.match(h[2])
    }
    bold_line_set    = {b[0] for b in bold_markers}
    desc_bold_set    = {b[0] for b in desc_bold_markers}
    boundary_lines   = sorted(heading_line_set | bold_line_set | desc_bold_set)

    def _slice_end(start: int) -> int:
        for ln in boundary_lines:
            if ln > start:
                return min(ln, start + 400)
        return min(n, start + 400)

    seen_names_at: set[Tuple[str, int]] = set()
    sections: List[Dict[str, str]] = []

    # ── Pass 1 — heading-style markers (existing behaviour) ───────────────
    for k, (i, depth, htext) in enumerate(headings):
        mh = _FB_NAME_HEADING_RE.match(lines[i].rstrip())
        if not mh:
            continue
        name = _unescape_md_name(mh.group("name"))
        # Must look like a register short-name: starts with an uppercase
        # letter and contains only letters/digits/underscores. Allow lowercase
        # index placeholders (e.g. ``CXPI_CHx_CTL1`` where ``x=0-3``).
        if not re.match(r"^[A-Z][A-Za-z0-9_]+$", name):
            continue
        # Reject English words (2+ consecutive lowercase letters) — true
        # register names are ALL-CAPS or have single lowercase placeholders.
        if re.search(r"[a-z]{2}", name):
            continue
        # Body slice: until the next register-start marker or H1-H3 heading.
        end = _slice_end(i)
        body = "\n".join(lines[i:end])
        if not _has_register_metadata(body):
            continue
        # Long name = inline " — desc" OR previous heading text if descriptive.
        long_name = (mh.group("desc") or "").strip()
        if not long_name and k > 0:
            prev_text = headings[k - 1][2]
            # Strip leading section numbering "1.7.9 "
            prev_text = re.sub(r"^\d+(?:\.\d+)*\s+", "", prev_text).strip()
            # Accept only if it doesn't look like another register name.
            if prev_text and not re.match(r"^[A-Z][A-Z0-9_\\]+$", prev_text):
                long_name = prev_text
        # Detect index-range annotation, e.g. "(x=0-3)" — enables expansion
        # of indexed-register sections into N concrete instances downstream.
        index_info: Optional[Dict[str, Any]] = None
        for src_text in (mh.group("name"), long_name, headings[k - 1][2] if k > 0 else ""):
            im = _INDEX_RANGE_RE.search(src_text or "")
            if im:
                lo, hi = int(im.group("lo")), int(im.group("hi"))
                if 0 <= lo <= hi <= 31:  # sanity
                    index_info = {
                        "letter": im.group("letter"),
                        "lo": lo,
                        "hi": hi,
                    }
                    break
        sections.append({
            "name": name,
            "text": body,
            "long_name": long_name,
            "index": index_info,
        })
        seen_names_at.add((name, i))

    # ── Pass 2 — bold-paragraph markers (RC1 2026-04 flavour) ─────────────
    for idx, (i, name, desc) in enumerate(bold_markers):
        if (name, i) in seen_names_at:
            continue
        end = _slice_end(i)
        body = "\n".join(lines[i:end])
        if not _has_register_metadata(body):
            continue
        # Long name: trailing " — desc" on the bold line, else the next
        # non-empty, non-bold line (which is typically the descriptive title
        # like "Reset control register A").
        long_name = desc
        if not long_name:
            for j in range(i + 1, min(i + 6, n)):
                ln = lines[j].strip()
                if not ln:
                    continue
                if ln.startswith("**") or ln.lower().startswith("offset address"):
                    break
                # Drop trailing " " markdown line-break.
                long_name = ln.rstrip()
                break
        # Detect index-range annotation, e.g. "(x=0-3)" on the bold marker.
        index_info: Optional[Dict[str, Any]] = None
        bold_raw = lines[i]
        for src_text in (bold_raw, long_name):
            im = _INDEX_RANGE_RE.search(src_text or "")
            if im:
                lo, hi = int(im.group("lo")), int(im.group("hi"))
                if 0 <= lo <= hi <= 31:
                    index_info = {
                        "letter": im.group("letter"),
                        "lo": lo,
                        "hi": hi,
                    }
                    break
        # Strip "(x=0-3)" from a name that still carries the placeholder so
        # downstream code sees the indexed form like "CXPI_CHx_CTL0".
        sections.append({
            "name": name,
            "text": body,
            "long_name": long_name,
            "index": index_info,
        })

    # ── Pass 3 — descriptive-bold markers (flavour 3, e.g. SENT) ─────────
    # These use the full register description in bold instead of the short
    # name. The LLM will extract the short name from the section content.
    seen_desc_lines = {s.get("_line") for s in sections if "_line" in s}
    for idx, (i, placeholder, desc_text) in enumerate(desc_bold_markers):
        if (placeholder, i) in seen_names_at:
            continue
        # Skip if a regular bold marker already claimed this line
        if i in bold_lines_already:
            continue
        end = _slice_end(i)
        body = "\n".join(lines[i:end])
        if not _has_register_metadata(body):
            continue
        # Detect index-range annotation in the descriptive text
        index_info: Optional[Dict[str, Any]] = None
        for src_text in (desc_text, lines[i]):
            im = _INDEX_RANGE_RE.search(src_text or "")
            if im:
                lo, hi = int(im.group("lo")), int(im.group("hi"))
                if 0 <= lo <= hi <= 31:
                    index_info = {
                        "letter": im.group("letter"),
                        "lo": lo,
                        "hi": hi,
                    }
                    break
        sections.append({
            "name": placeholder,
            "text": body,
            "long_name": desc_text,
            "index": index_info,
        })

    # Sort by appearance order in the document so downstream consumers see a
    # natural ordering.
    sections.sort(key=lambda s: content.find(s["text"]) if s["text"] else 0)
    return sections


def _has_register_metadata(section_text: str) -> bool:
    lower = section_text.lower()
    if "offset address" not in lower:
        return False
    # Reject overview tables (they reference many registers in one block).
    if section_text.count("Offset address") > 4:
        return False
    return True


def _clean_section_text(text: str) -> str:
    """Strip page-break markers, diagram explanations, repeated headers."""
    out: List[str] = []
    in_diagram = False
    for line in text.split("\n"):
        stripped = line.strip()
        if re.match(r"^#{1,2}\s+Pages?\s+\d+", stripped):
            continue
        if re.match(r"^#{1,2}\s+\d+\s+", stripped):
            continue
        if stripped == "(continued)" or stripped.startswith("*(table continues"):
            continue
        if (stripped.startswith("### Diagram explanation")
                or stripped.startswith("### Figure:")
                or stripped.startswith("**What is this diagram showing")
                or stripped.startswith("**What this diagram")):
            in_diagram = True
            continue
        if in_diagram:
            if (stripped.startswith("|")
                    or stripped.startswith("###")
                    or "offset address" in stripped.lower()
                    or "reset value" in stripped.lower()):
                in_diagram = False
            else:
                continue
        out.append(line)
    return "\n".join(out)


def _llm_extract_section(
    client, model: str, section: Dict[str, str], retry: int = 0,
) -> Optional[Dict[str, Any]]:
    """Send one register section to the LLM and parse the JSON reply.

    Results are cached on disk keyed by SHA-256 of (model, prompt_version,
    system prompt, user content, seed). Re-runs return the cached JSON
    object directly, making the pipeline deterministic and fast.
    """
    text = _clean_section_text(section["text"])
    cache_key = _llm_cache_key(
        model=model,
        system_prompt=_EXTRACTION_PROMPT,
        user_content=text,
        seed=_LLM_SEED,
        extra="register-section",
    )
    cached = _llm_cache_get(model, cache_key)
    if cached is not None:
        _llm_cache_record(hit=True)
        return cached
    _llm_cache_record(hit=False)
    try:
        resp = client.chat.completions.create(
            model=model,
            temperature=0,
            seed=_LLM_SEED,
            max_completion_tokens=5000,
            messages=[
                {"role": "system", "content": _EXTRACTION_PROMPT},
                {"role": "user", "content": text},
            ],
        )
        raw = (resp.choices[0].message.content or "").strip()
        if raw.startswith("```"):
            raw = re.sub(r"^```(?:json)?\s*\n?", "", raw)
            raw = re.sub(r"\n?```\s*$", "", raw)
        parsed = json.loads(raw)
        _llm_cache_put(model, cache_key, parsed)
        return parsed
    except json.JSONDecodeError as e:
        if retry < _MAX_RETRIES - 1:
            logger.warning(
                "JSON parse error for %s (attempt %d): %s — retrying",
                section["name"], retry + 1, e,
            )
            time.sleep(2)
            return _llm_extract_section(client, model, section, retry + 1)
        logger.error("LLM JSON parse failed for %s after %d retries",
                     section["name"], _MAX_RETRIES)
        return None
    except Exception as e:  # network / API error
        if retry < _MAX_RETRIES - 1:
            is_rate = "429" in str(e) or "Rate limit" in str(e)
            wait = 30 if is_rate else 5
            logger.warning(
                "LLM call failed for %s (attempt %d): %s — retrying in %ds",
                section["name"], retry + 1, e, wait,
            )
            time.sleep(wait)
            return _llm_extract_section(client, model, section, retry + 1)
        logger.error("LLM extraction failed for %s after %d retries: %s",
                     section["name"], _MAX_RETRIES, e)
        return None


_LLMExtractionResult = Tuple[
    List[Dict[str, Any]],  # registers
    List[Dict[str, Any]],  # fields
    List[Dict[str, Any]],  # interrupts
    List[Dict[str, Any]],  # errors
    List[Dict[str, Any]],  # hw_constraints
    str,                   # clean_text (concatenated clean_prose)
]


def _llm_extract_all(
    content: str,
    *,
    model: str = _DEFAULT_MODEL,
    max_workers: int = 3,
    api_key: Optional[str] = None,
) -> _LLMExtractionResult:
    """Run the LLM pass over an FB-content markdown.

    Returns ``(registers, fields, interrupts, errors, hw_constraints,
    clean_text)``.  Returns empty lists + ``""`` when no candidate section
    is found or when the LLM client cannot be initialised — caller should
    fall back to regex.
    """
    empty: _LLMExtractionResult = ([], [], [], [], [], "")
    sections = _split_fbcontent_sections(content)
    logger.info("LLM section split: %d candidate register sections", len(sections))
    if not sections:
        return empty

    try:
        token = api_key or _llm_get_token()
        client = _llm_build_client(token, model)
    except Exception as e:
        logger.warning("LLM client unavailable (%s) — falling back to regex", e)
        return empty

    rate_limiter = _RateLimiter(_RATE_LIMIT_RPM)
    extracted: List[Optional[Dict[str, Any]]] = [None] * len(sections)
    _llm_cache_reset_stats()

    def _process(idx: int):
        rate_limiter.acquire()
        return idx, _llm_extract_section(client, model, sections[idx])

    t0 = time.time()
    with ThreadPoolExecutor(max_workers=max_workers) as pool:
        futures = {pool.submit(_process, i): i for i in range(len(sections))}
        done = 0
        for fut in as_completed(futures):
            idx, res = fut.result()
            extracted[idx] = res
            done += 1
            if done % 5 == 0 or done == len(sections):
                logger.info("  LLM progress: %d/%d", done, len(sections))
    elapsed = time.time() - t0
    success = sum(1 for r in extracted if r is not None)
    hits, misses = _llm_cache_get_stats()
    logger.info("LLM extraction: %d/%d sections successful in %.1fs "
                "(cache hits=%d, misses=%d)",
                success, len(sections), elapsed, hits, misses)

    registers: List[Dict[str, Any]] = []
    fields: List[Dict[str, Any]] = []
    interrupts: List[Dict[str, Any]] = []
    errors: List[Dict[str, Any]] = []
    constraints: List[Dict[str, Any]] = []
    clean_chunks: List[str] = []
    seen_regs: set = set()
    seen_intr: set = set()
    seen_err: set = set()
    seen_con: set = set()
    constraint_counter = 0

    _ALLOWED_CON_KINDS = {
        "timing", "wait_loop", "ordering",
        "access_width", "frequency", "side_effect",
    }
    _ALLOWED_ERR_TYPES = {
        "transmission", "reception", "bus",
        "data_integrity", "timing", "general",
    }

    for sec_idx, item in enumerate(extracted):
        if not item:
            continue
        reg = item.get("register")
        if not reg or not isinstance(reg, dict):
            continue
        base_name = (reg.get("name") or "").strip()
        if not base_name:
            continue

        sec = sections[sec_idx]
        idx_info = sec.get("index")
        if idx_info:
            index_values: List[Optional[int]] = list(
                range(idx_info["lo"], idx_info["hi"] + 1)
            )
            letter: Optional[str] = idx_info["letter"]
        else:
            index_values = [None]
            letter = None

        for idx_val in index_values:
            name = _xf(base_name, letter, idx_val)
            if not name or name in seen_regs:
                continue
            seen_regs.add(name)
            long_name = _xf(reg.get("long_name") or "", letter, idx_val)
            section_title = _xf(sec.get("long_name") or name, letter, idx_val)
            registers.append({
                "name": name,
                "long_name": long_name,
                "offset": reg.get("offset"),
                "reset_value": reg.get("reset_value"),
                "reset_type": reg.get("reset_type") or "kernel",
                "index": idx_val,
                "base_name": base_name if idx_val is not None else None,
            })
            # Fields — dedup by (name, register) within the same register
            # body so duplicate rows produced by the LLM (e.g. when two table
            # rows describe the same field) collapse to a single entry.
            _seen_field_keys: set = set()
            for f in (item.get("fields") or []):
                fname = (f.get("name") or "").strip()
                if not fname:
                    continue
                key_f = (fname, name)
                if key_f in _seen_field_keys:
                    continue
                _seen_field_keys.add(key_f)
                fields.append({
                    "name": fname,
                    "parent_register": name,
                    "bits": (f.get("bits") or "").strip(),
                    "type": (f.get("type") or "").strip(),
                    "description": ((f.get("description") or "")[:200]).strip(),
                })
            # Interrupts — dedup by (name, register) so per-channel copies survive
            for it in (item.get("interrupts") or []):
                iname = (it.get("name") or "").strip()
                if not iname:
                    continue
                key_i = (iname, name)
                if key_i in seen_intr:
                    continue
                seen_intr.add(key_i)
                bit_raw = it.get("bit")
                bit = bit_raw if isinstance(bit_raw, int) else None
                interrupts.append({
                    "name": iname,
                    "register": name,
                    "bit": bit if bit is not None else 0,
                    "description": ((it.get("description") or "")[:200]).strip(),
                })
            # Errors — dedup by (name, detected_in)
            for er in (item.get("errors") or []):
                ename = (er.get("name") or "").strip()
                if not ename:
                    continue
                key_e = (ename, name)
                if key_e in seen_err:
                    continue
                seen_err.add(key_e)
                etype = (er.get("type") or "general").strip().lower()
                if etype not in _ALLOWED_ERR_TYPES:
                    etype = "general"
                bit_raw = er.get("bit")
                bit = bit_raw if isinstance(bit_raw, int) else None
                errors.append({
                    "name": ename,
                    "type": etype,
                    "description": ((er.get("description") or "")[:200]).strip(),
                    "detected_in": name,
                    "bit": bit,
                })
            # HW constraints
            for con in (item.get("hw_constraints") or []):
                kind = (con.get("kind") or "").strip().lower()
                if kind not in _ALLOWED_CON_KINDS:
                    continue
                condition = _xf(
                    ((con.get("condition") or "")[:300]).strip(),
                    letter, idx_val,
                )
                if not condition:
                    continue
                tgt_field = (con.get("target_field") or "").strip() or None
                value = con.get("value")
                value = str(value).strip() if value not in (None, "") else None
                unit = con.get("unit")
                unit = str(unit).strip() if unit not in (None, "") else None
                key = (kind,
                       "RegisterField" if tgt_field else "HardwareRegister",
                       tgt_field or name, value, condition[:80])
                if key in seen_con:
                    continue
                seen_con.add(key)
                constraint_counter += 1
                constraints.append({
                    "kind": kind,
                    "name": f"{name}_{kind}_{constraint_counter}",
                    "target_label":
                        "RegisterField" if tgt_field else "HardwareRegister",
                    "target_name": tgt_field or name,
                    "target_field": tgt_field,
                    "condition": condition,
                    "source_section": section_title,
                    "value": value,
                    "unit": unit,
                })
            # Clean prose
            prose = (item.get("clean_prose") or "").strip()
            if prose:
                prose_expanded = _xf(prose, letter, idx_val)
                clean_chunks.append(
                    f"## {section_title} ({name})\n{prose_expanded}"
                )

    clean_text = "\n\n".join(clean_chunks)
    logger.info(
        "LLM yielded %d registers, %d fields, %d interrupts, %d errors, "
        "%d constraints, %d clean-prose chunks (%d chars)",
        len(registers), len(fields), len(interrupts), len(errors),
        len(constraints), len(clean_chunks), len(clean_text),
    )
    return registers, fields, interrupts, errors, constraints, clean_text


